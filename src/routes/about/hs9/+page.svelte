<h1>Introduction</h1>
<p>
    HackSheffield is the University of Sheffield's annual hackathon; a weekend of hacking, games and networking!
    In 2024, I got the honour of organising the 9th iteration of HackSheffield, alongside the rest of the team at <a href="/about/compsoc" target="_blank">CompSoc</a>.
</p>

<h2>Joining the Subcommittee</h2>
<p>
    After being elected as First Year Rep for CompSoc in October 2024, I was invited to join the HackSheffield 9 Subcommittee as an organiser.
    From there, I eventually carved out the role of Technical Lead.
</p>
<p>
    Under this new role, I took responsibility for the event's technical needs.
    This included automating hacker registration on Discord, arranging AV equipment for the opening and closing ceremonies, and ensuring ticket-checking went smoothly.
</p>

<h2>During the Event</h2>
<p>
    As one of the main organisers, I was very involved with the event on-the-day.
    I assisted with the setup and teardown of the venue, helped with on-the-door registration, and helped prepare the food for the attendees.
</p>

<h2>Reviewing the Event</h2>
<p>
    Throughout my journey with HackSheffield 9, I kept a record of complaints, failures and successes that the organising team encountered.
    This was used in the Subcommittee's review meeting, which addressed what went well, what didn't, and how we can improve for HackSheffield 10.
</p>