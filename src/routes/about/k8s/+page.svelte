<h1>Introduction</h1>
<p>
    <a href="https://kubernetes.io" target="_blank">Kubernetes</a> is a container orchestration platform used by companies all over the world to scale their software across entire datacenters.
    It's a highly sought-after skill in the industry, in part because of its steep learning curve that challenges almost everyone that tries to learn it.
</p>
<p>
    Having self-taught Kubernetes in Summer 2024, using my homelab <a href="/about/constellation" target="_blank">Constellation</a>, I decided I wanted to share my knowledge with others.
</p>

<h2>What the Workshop Covers</h2>
<p>
    My workshop introduced attendees to the basics of Kubernetes and containerisation, who uses Kubernetes and why they should learn it.
    The main focuses of the workshop were:
</p>
<ul>
    <li>Clustering and setting up a basic cluster with <a href="https://www.docker.com/products/docker-desktop/" target="_blank">Docker Desktop</a></li>
    <li>Manifests and what they mean within a cluster</li>
    <li>Interacting with a cluster with kubectl</li>
    <li>Pods and containers</li>
    <li>Replica Sets and its derivatives</li>
    <li>Basic Pod networking with Services</li>
</ul>

<h2>What the Workshop Doesn't Cover</h2>
<p>
    Unfortunately I couldn't cover everything in my workshop. Were I to run a more advanced sequel to this workshop, the topics I'd include would be:
</p>
<ul>
    <li>PersistentVolumes and PVClaims</li>
    <li>CD tools like <a href="https://www.google.com/search?client=safari&rls=en&q=argocd&ie=UTF-8&oe=UTF-8" target="_blank">ArgoCD</a> and <a href="https://fluxcd.io" target="_blank">FluxCD</a></li>
    <li>Ingress and <a href="https://metallb.io" target="_blank">MetalLB</a></li>
</ul>

